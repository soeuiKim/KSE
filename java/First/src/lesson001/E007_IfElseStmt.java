package lesson001;

public class E007_IfElseStmt {

	public static void main(String[] args) {
		// If...else... Statement
		// if (조건) {...} else {...} _ 두개의 선택지

		int age = 17;
		
		//if else 구문의 예
		if(age >= 20) {
			System.out.println("나이먹음ㅋ_출입가능");
		} else {
			System.out.println("어림ㅋ_꺼졍");
		}//end of if(age>20)
		
		System.out.println("일하기시름 안함 빠잉");
		
	}//end of main

}//end of class
